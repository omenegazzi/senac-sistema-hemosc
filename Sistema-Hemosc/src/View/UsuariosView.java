    public void CarregarTabela() {

        DefaultTableModel tabela = (DefaultTableModel) tUsuarios.getModel();
        UsuariosDao Dao = new UsuariosDao();
        tabela.setNumRows(0);

        for (Usuarios u : Dao.listar()) {
            tabela.addRow(new Object[]{
                u.getCodigo(),
                u.getNome(),
                u.getEmail(),
                u.getSenha()
            });
        }
    }

